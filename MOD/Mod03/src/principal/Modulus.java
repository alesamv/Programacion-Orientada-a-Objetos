// Demonstrate the % operator.
package principal;
public class Modulus {
   public static void main(String args[]) {
      int x = 42;
      double y = 75;
      double z = 3.1416;
      System.out.println(x + " mod 10 = " + x % 10);
      System.out.println(y + " mod 10 = " + y % 10);
      System.out.println(z + " mod 4.55 = " + y % 4.55);
   }
}
